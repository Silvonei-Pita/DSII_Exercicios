
package Ingresso;


public class Ingresso {
    //Atributos
    protected float valor;
    private String nomeCliente;
    private String cpf;

    //Construtor
    public Ingresso(float valor, String nomeCliente, String cpf) {
        this.valor = valor;
        this.nomeCliente = nomeCliente;
        this.cpf = cpf;
    }

    //Getters 
    
    public float getValor() {
        return valor;
    }

    public String getNomeCliente() {
        return nomeCliente;
    }

    public String getCpf() {
        return cpf;
    }

    
 
    
    
    

    
    
    
    
    
    
    
    
    
    
}
